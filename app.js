// WebSocket baƒülantƒ±sƒ±
function connectWS() {
  ws = new WebSocket(WS_URL);
  ws.onopen = () => console.log('‚úÖ WebSocket baƒülantƒ±sƒ± kuruldu');
  ws.onmessage = (e) => {
    const data = JSON.parse(e.data);
    if (data.type === "message") addMessage(data.user || data.nick, data.text);
  };
  ws.onclose = () => setTimeout(connectWS, 3000);
  ws.onerror = (error) => console.error('‚ùå WebSocket hatasƒ±:', error);
}

// Mesaj ekleme
function addMessage(user, text) {
  const messages = document.getElementById('messages');
  if (messages) {
    const div = document.createElement('div');
    const time = new Date().toLocaleTimeString();
    div.innerHTML = `<span class="time">[${time}]</span> <b>${user}:</b> ${text}`;
    messages.appendChild(div);
    messages.scrollTop = messages.scrollHeight;
  }
}

// Kanal deƒüi≈ütirme
function switchChannel(channelName) {
  currentChannel = channelName;
  const channelHeader = document.getElementById('currentChannel');
  if (channelHeader) channelHeader.textContent = channelName;
  const messages = document.getElementById('messages');
  if (messages) messages.innerHTML = `<div class="info">üöÄ ${channelName} kanalƒ±na ho≈ü geldiniz!</div>`;
  console.log('üì∫ Kanal deƒüi≈ütirildi:', channelName);
}

